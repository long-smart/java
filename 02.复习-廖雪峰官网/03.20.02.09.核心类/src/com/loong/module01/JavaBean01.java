package com.loong.module01;

public class JavaBean01 {
    private String name = "123";
    private int age = 12;

    public JavaBean01(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
